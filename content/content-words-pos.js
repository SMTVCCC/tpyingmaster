// 获取单词词性的函数
window.getWordPartOfSpeech = function(word, mode) {
  let posData;
  
  switch(mode) {
    case 'words_junior':
      posData = window.CONTENT_WORDS_JUNIOR_POS;
      break;
    case 'words_high':
      posData = window.CONTENT_WORDS_HIGH_POS;
      break;
    case 'words_toefl':
      posData = window.CONTENT_WORDS_TOEFL_POS;
      break;
    default:
      posData = window.CONTENT_WORDS_JUNIOR_POS;
  }
  
  return posData[word] || '';
};